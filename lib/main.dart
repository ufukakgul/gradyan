  Widget build(BuildContext context) {
    return Scaffold(
      body: Container(
        decoration: BoxDecoration(
            gradient: LinearGradient(
          begin: Alignment.topRight,
          end: Alignment.bottomRight,
          stops: const [
            0.1,
            0.4,
            0.8,
            0.9,
          ],
          colors: [
            Colors.blue.shade800,
            Colors.blue.shade600,
            Colors.red.shade200,
            Colors.red.shade100,
          ],
        )),
      ),
    );
  }
}
